public class Output {
    public static void main(String[] args) {
        /*
        Printing an output works with the System.out.println (sout* shortcode).
        You can also use System.out.print to print in the same line as the previous one.
         */
        System.out.println("Hello world");
        System.out.print("Hello + ");
        System.out.println("World");

        /*
        all text must be wrapped inside of double quotes -> "Hello World".
        without, you will get an error.
         */
    }
}
